def countdown(start_num)
  current_num = start_num
  while current_num > 0 do
    puts "#{current_num} SECOND(S)!"
    current_num -= 1
  end
  "HAPPY NEW YEAR!"
end

def countdown_with_sleep(start_num)
  current_num = start_num
  while current_num > 0 do
    puts "#{current_num} SECOND(S)!"
    current_num -= 1
    sleep 1
  end
  "HAPPY NEW YEAR!"
end
